package main

import (
	"fmt"
	"math"
)

func main() {
	fmt.Println("Square Root of 16:", math.Sqrt(16))
	fmt.Println("Absolute value of -7.5:", math.Abs(-7.5))
	fmt.Println("2 raised to the power 5:", math.Pow(2, 5))
}

/*
Square Root of 16: 4
Absolute value of -7.5: 7.5
2 raised to the power 5: 32
*/
