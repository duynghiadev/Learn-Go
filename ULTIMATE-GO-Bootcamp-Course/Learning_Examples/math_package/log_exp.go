package main

import (
	"fmt"
	"math"
)

func main() {
	fmt.Println("Natural Logarithm of 10:", math.Log(10))
	fmt.Println("Base-10 Logarithm of 1000:", math.Log10(1000))
	fmt.Println("Exponent of 2 (e^2):", math.Exp(2))
}

/*
Natural Logarithm of 10: 2.302585092994046
Base-10 Logarithm of 1000: 3
Exponent of 2 (e^2): 7.38905609893065
*/
